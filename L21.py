

def add(a, b):
	print(f"Adding {a} + {b}")
	return a + b
	
def minus(a, b):
	print(f"Subtracing {a} - {b}")
	return a -b
	
def multiply(a, b):
	print(f"Multiplying {a} * {b}")
	return a * b
	
def divide(a, b):
	print(f"Dividing {a} / {b}")
	return a /b
	
	
age = add(30, 5)
height = minus(78, 4)
weight = multiply(90, 2)
iq = divide(100, 2)	

print(f"Age: {age}, \nHeight: {height}, \nWeight: {weight},\niq: {iq}")

